#include<iostream>
int main(){
  std::cout << "Size of: int = " << sizeof(int) << '\n';
  std::cout << "Size of: short = " << sizeof(short) << '\n';
  std::cout << "Size of: long = " << sizeof(long) << '\n';
  std::cout << "Size of: char = " << sizeof(char) << '\n';
  std::cout << "Size of: bool = " << sizeof(bool) << '\n';
  return 0;
}
